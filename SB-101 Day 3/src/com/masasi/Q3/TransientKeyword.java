package com.masasi.Q3;

public class TransientKeyword {
		
	
//	Transient keyword is use to marks the member of an object not to serialize
//	When we serialize the object it converts in to  the byte code by which its
//	easy to transfer the object through the networks and by process of the 
//	de-serialization object is recreated from that byte code 
	
//	But if the member of an object is marked as the transient it will not store in the 
//	byte code while the time of the serialization 
//	Whenever JVM encounters with keyword "transient" JVM will simply ignore it not be 
//	serialized
}
